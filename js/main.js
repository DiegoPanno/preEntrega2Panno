const contenedorProductos = document.querySelector("#contenedorProducto");
const menuCategoria = document.querySelectorAll(".boton-categoria");
let btnProductoComprar = document.querySelectorAll(".btnProducto");
const numerito = document.querySelector("#numerito");
const tituloPrincipal = document.querySelector("#tituloPrincipal");




function cargarProductos(productosElegidos) {

    contenedorProductos.innerHTML = "";

    productosElegidos.forEach(producto => {
        const div = document.createElement("div");
        div.classList.add('tarjetas-producto');
        div.innerHTML = `
        <img class="producto-imagen" src="${producto.imagen}" alt="${producto.titulo}">
        <div class="producto-detalle">
           <h2 class="producto-titulo">${producto.titulo}</h2>
           <p class="producto-precio">$${producto.precio}</p>
           <button class="btnProducto" id="${producto.id}">Comprar!</button>
        </div>
        `;

        contenedorProductos.append(div);


    })
    actualizarbtnProductoComprar();

}


cargarProductos(productos);



menuCategoria.forEach(boton => {
    
    
    boton.addEventListener("click", (e) => {
        e.currentTarget.classList.add("active");
        menuCategoria.forEach(boton => boton.classList.remove("active"));


        if (e.currentTarget.id != "todos") {
            const productoCategoria = productos.find(producto => producto.categoria.id === e.currentTarget.id);
            tituloPrincipal.innerText = productoCategoria.categoria.nombre;

            const productoBoton = productos.filter(producto => producto.categoria.id === e.currentTarget.id);
            cargarProductos(productoBoton);


        } else {
            tituloPrincipal.innerText = "Todos los Productos";
            cargarProductos(productos);
        };

        
    })
})






function actualizarbtnProductoComprar() {
    btnProductoComprar = document.querySelectorAll('.btnProducto');

    btnProductoComprar.forEach(boton => {
        boton.addEventListener("click", agregarAlCarrito);
    });

}


let productoEnCarrito;
let productoEnCarritoLS = localStorage.getItem("prod");

if (productoEnCarritoLS) {
    productoEnCarrito = JSON.parse(productoEnCarritoLS);
    actualizarNumerito();
} else {
    productoEnCarrito = [];
}

function agregarAlCarrito(e) {

        Toastify({
          text: "Agregaste un producto 😃",
          duration: 3000,
          destination: "../pages/carritodecompra.html",
          close: true,
          gravity: "top", // `top` or `bottom`
          position: "right", // `left`, `center` or `right`
          stopOnFocus: true, 
          style: {
            color: "rgb(6, 6, 6)",
          background: "rgb(0, 255, 255)",
          borderRadius: "2rem",
          textTransform: "uppercase",
          fontSize: ".75rem"
          },
          offset: {
            x: '1.5rem',
            y: '1.5rem' 
          },
         onClick: function(){} 
         }).showToast();

    const idBoton = e.currentTarget.id;
    const productoAgregado = productos.find(producto => producto.id === idBoton);

    if (productoEnCarrito.some(producto => producto.id === idBoton)) {
        const index = productoEnCarrito.findIndex(producto => producto.id === idBoton);
        productoEnCarrito[index].cantidad++;

    } else {
        productoAgregado.cantidad = 1;
        productoEnCarrito.push(productoAgregado);
    }

    actualizarNumerito();

    localStorage.setItem("prod", JSON.stringify(productoEnCarrito));

}

function actualizarNumerito() {
    let nuevoNumerito = productoEnCarrito.reduce((acc, producto) => acc + producto.cantidad, 0);
    numerito.innerText = nuevoNumerito;


}  